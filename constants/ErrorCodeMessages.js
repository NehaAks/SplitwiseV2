export default function ErrorCodeMessages(code)
{
    if(code === 400)
    {
        return 'Due to technical issue, we cannot process your request now. Please try again later.'
    }
    else if(code === 403)
    {
        return 'Due to technical issue, we cannot process your request now. Please try again later.'
    }
    else if(code === 404)
    {
        return 'Due to technical issue, we cannot process your request now. Please try again later.'
    }
    else if(code === 500)
    {
        return 'Due to technical issue, we cannot process your request now. Please try again later.'
    }
}